package com.websharp.entity;

public class EntityGroup {

	public String InterviewName = "";
	public String StartTime = "";
	public String ExpiredTime = "";
	public int PlanCount = 0;
	public int FactCount = 0;
	public String ScoreTimes = "";
	public int InterviewID = 0;
	public String GroupName = "";
	public int GroupProperty = 0;
	public int State = 0;
	public int ID = 0;

	public String getInterviewName() {
		return InterviewName;
	}

	public void setInterviewName(String interviewName) {
		InterviewName = interviewName;
	}

	public String getStartTime() {
		return StartTime;
	}

	public void setStartTime(String startTime) {
		StartTime = startTime;
	}

	public String getExpiredTime() {
		return ExpiredTime;
	}

	public void setExpiredTime(String expiredTime) {
		ExpiredTime = expiredTime;
	}

	public int getPlanCount() {
		return PlanCount;
	}

	public void setPlanCount(int planCount) {
		PlanCount = planCount;
	}

	public int getFactCount() {
		return FactCount;
	}

	public void setFactCount(int factCount) {
		FactCount = factCount;
	}

	public String getScoreTimes() {
		return ScoreTimes;
	}

	public void setScoreTimes(String scoreTimes) {
		ScoreTimes = scoreTimes;
	}

	public int getInterviewID() {
		return InterviewID;
	}

	public void setInterviewID(int interviewID) {
		InterviewID = interviewID;
	}

	public String getGroupName() {
		return GroupName;
	}

	public void setGroupName(String groupName) {
		GroupName = groupName;
	}

	public int getGroupProperty() {
		return GroupProperty;
	}

	public void setGroupProperty(int groupProperty) {
		GroupProperty = groupProperty;
	}

	public int getState() {
		return State;
	}

	public void setState(int state) {
		State = state;
	}

	public int getID() {
		return ID;
	}

	public void setID(int iD) {
		ID = iD;
	}

}
